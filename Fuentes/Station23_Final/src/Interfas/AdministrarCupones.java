    private void jbtAñadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtAñadirActionPerformed
        Double descNuevo = Double.valueOf(jtfDescuento.getText().replace(",", "."));
        Cupon c = new Cupon();
        c.setId_cupon(jtfIDCupon.getText());
        c.setDescuento(descNuevo);
        Cupon cup = daoRegistro.buscarCupon(jtfIDCupon.getText());
        if (cup == null) {
            daoRegistro.insertarCupon(c);
            jtbTarifario.setModel(imprimirDatos());
            vaciar();
        }else{
            JOptionPane.showMessageDialog(null, "El ID del cupon que intenta ingresar ya esta registrado, intente con otro.");
        }

    }//GEN-LAST:event_jbtAñadirActionPerformed

    private void jbtEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtEliminarActionPerformed
        daoRegistro.eliminarCupon(jtfIDCupon.getText());
        jtbTarifario.setModel(imprimirDatos());
        vaciar();
    }//GEN-LAST:event_jbtEliminarActionPerformed


