    private DefaultTableModel imprimirDatos() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID del Cupon");
        modelo.addColumn("Descuento (S/.)");
        List<Cupon> cupons = daoRegistro.cuponSel();
        for (Cupon c : cupons) {
            String desc = String.format("%.2f", c.getDescuento());
            modelo.addRow(new Object[]{c.getId_cupon(), desc});
        }
        return modelo;
    }

    private void vaciar() {
        jtfDescuento.setText("");
        jtfIDCupon.setText("");
    }

